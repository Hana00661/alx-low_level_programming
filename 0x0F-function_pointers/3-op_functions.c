#include "3-clac.h"

/**
 * op_add - add oparation
 *
 * @a: the first num
 * @b: the second num
 *
 * Return: the sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub oparation
 *
 * @a: the first num
 * @b: the second num
 *
 * Return: the subtraction
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiblay oparation
 *
 * @a: the first num
 * @b: the second num
 *
 * Return: the prodect
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - dividing oparation
 *
 * @a: the first num
 * @b: the second num
 *
 * Return: the quotient
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module
 *
 * @a: the first num
 * @b: the second num
 *
 * Return: the reminder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
